#include <stdio.h>

/**
 * main - program that prints all single digit numbers of base 10 starting from 0
 * You are not allowed to use any variable of type char
 * Use only the putchar function twice
 * Return: 0
 */

int main(void)
{
int n = 48;
while (n <= 57)
{
putchar(n);
n ++;
}
printf("\n");
return (0);
}
